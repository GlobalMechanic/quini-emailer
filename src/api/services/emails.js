import { Service, Hook } from '@benzed/app'

/******************************************************************************/
// Hooks
/******************************************************************************/

const webshotHook = new Hook({

  before: 'all',
  priority: 0

})

/******************************************************************************/
// Main
/******************************************************************************/

class EmailService extends Service {

  hooks () {
    super.hooks()

    return [
      webshotHook
    ]
  }

}

/******************************************************************************/
// Exports
/******************************************************************************/

export default new EmailService()
